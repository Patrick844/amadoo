// Reads design/icons/*.svg and emits components/ui/iconRegistry.ts — a name->SVG-xml map.
// Icons are Lucide SVGs (stroke="currentColor"), tinted at runtime via <Icon color>.
// Re-run after adding/removing an icon:  node scripts/gen-icon-registry.mjs
import { readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const iconsDir = join(root, 'design/icons')
const out = join(root, 'components/ui/iconRegistry.ts')

const files = readdirSync(iconsDir).filter((f) => f.endsWith('.svg')).sort()
const entries = files.map((f) => {
  const name = f.replace(/\.svg$/, '')
  let xml = readFileSync(join(iconsDir, f), 'utf8')
    .replace(/<!--[\s\S]*?-->/g, '') // strip license comment
    .replace(/\s+/g, ' ')            // collapse whitespace
    .trim()
  return `  '${name}': ${JSON.stringify(xml)},`
})

const body = `// AUTO-GENERATED by scripts/gen-icon-registry.mjs — do not edit by hand.
// Source: design/icons/*.svg (Lucide, ISC). Tint via <Icon color>.
export const ICONS = {
${entries.join('\n')}
} as const

export type IconName = keyof typeof ICONS
`
writeFileSync(out, body)
console.log(`Wrote ${files.length} icons -> components/ui/iconRegistry.ts`)
